export function assertSeedCoverage(teamIds: readonly TeamId[]): void {
  if (SEED_TEAMS.length !== 30) {
    throw new Error(`SEED_TEAMS must have 30 entries, got ${SEED_TEAMS.length}`);
  }
  const ids = SEED_TEAMS.map((t) => t.identity.teamId);
  const missing = teamIds.filter((id) => !ids.includes(id));
  if (missing.length) {
    throw new Error(`Missing seed teams: ${missing.join(", ")}`);
  }
}
